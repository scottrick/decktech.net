        <tr><td><font face="Tolk, Times, serif" class="h1font"><font color="#82A482">Les dégâts directs à Lord Of The Rings TCG</font></B></font></td></tr></table></td></tr><tr><td width="2"></td><td valign="top"> 
      <table width="100%" border="0" cellspacing="0" cellpadding="5"><tr><td><font face="  Times, Times New Roman, serif" class="bfont"> By: Marc 'Witch-King' THERY. 
            <p> <a href="http://www.decipher.com/lordoftherings/cardlists/fellowship/large/209bladetip.html" target="_blank"><img src="../gfx/blade_tipcard.jpg" width="200" height="240" align="right" border="0"></a>
Dans la plupart des JCCs, une tactique communément utilisée pour gagner est d'infliger des dégâts directs aux personnages d'un joueur. Par dégâts directs, on entend : 'dégâts qu'ils ne peuvent éviter'. Dans le JCC Lord Of The Rings (LOTR CCG), cette tactique est aussi disponible, comme nous le verrons ci-dessous. Dans ce jeu, un personnage blessé (en particulier un allié) ne pourra pas toujours faire un 'exert' et utiliser son game text. On affaiblira aussi (et cela tuera peut être) quelques compagnions principaux pour dissuader l'adversaire d'avancer trop rapidement (de deux sites ou plus par tour). Pour rappel, dans LOTR CCG, il y a deux sortes de dommages que l'adversaire peut recevoir : les blessures 'wounds' et les points de corruption 'burdens'. Infliger l'un, l'autre ou les deux mène à la victoire.<p>

Voici les différentes façons d'infliger des blessures directes :
<br>
La plus évidente est par l'utilisation des archers. Bien que l'on ne puisse pas réellement choisir quels personnages adverses vont être touchés, les dégâts sont réels et il n'y a même pas besoin de gagner de combats pour les faire ! Aussi, toute carte augmentant votre total d'archerie  (ou permettant de choisir une cible précise) est utile. En voici quelques unes,  à la fois pour le 'Free People Player' et le 'Shadow Player':<p>
Free People Player (FPP) :<br>
Legolas, GreenLeaf (usage évident).  Numéro de carte  (NC) : 50.<br>
Aragorn's Bow => donne un Aragorn, Greenleaf. :-) Voir Legolas. NC : 90.<br>
Orophin ou  Rùmill (un peu moins efficaces). NCs : 56 & 57.<br>
Double Shot (ajoute 1 à l'archerie). NC : 38.<br>
The Splendor Of Their Banners. (exert de l'archer pour ajouter 1 à l'archerie. Utile surtout pour améliorer quelqu'un devenu archer) NC : 62.<p>
Shadow Player :<br>
Bitter Hatred (Montrer un archer pour blesser un elfe. NB : cela n'influe pas sur votre total d'archerie ! ). NC : 164.<br>
Orc Bowmen (Très efficaces si vous jouez avec des Orcs de Sauron, avec ou sans archers). NC : 264.<br>
<p>
Hormis l'archerie, il y a de nombreuses autres cartes pour infliger des blessures. L'idéal étant, bien sûr, de maximiser les dégâts avec le minimum de cartes.<p>
FPP :<br>
Here Lies Balin, Son of Fundin (Blesse deux orcs ou un orc deux fois). NC : 19.<br>
Foes Of Mordor (exert un compagnion homme pour blesser un minion de Sauron). NC : 105<br>
Blade Of Gondor (Enfin un Boromir utile !:-) ). NC : 95.<br>
Power According To His Stature ( à jouer si vous avez un deck avec un super Frodo :-) ). NC : 308.<p>
SP :<br>
Relentless Charge (Blesse tous les archers si vous exertez un Nàzgül ). NC : 223<br>
Their Power Is In Terror (Une des meilleures cartes , car les alliés ont généralement deux points de vitalité et mourront ou ne pourront plus s'exerter). NC : 226.<br>
Hate (Blesse un compagnion de votre choix). NC : 250<br>
Ulaïre Enquëa, Lieutenant of Morgul. NC : 231.<br>
Mordor's Strengh (Exerter un orc de Sauron pour blesser un personnage). NC : 255.<br>
Orc Hunters, Orc Scouting Band, Orc Soldier. NCs : 267, 270 & 271. (Chacun d'eux peut blesser en combat)<p>
Quelques cartes, comme Saruman's Chills (NC : 134), sont beaucoup plus vicieuses, car elles forcent un adversaire à exerter un ou plusieurs de ces personnages. La plupart de ces cartes sont des sites (cf ci-dessous).
<p>
La plus puissante 'combo' à l'heure actuelle est de jouer Under The Watching Eye (NC : 281) combinée à plusieurs You Bring Great Evil (NC : 283) ou, si vous jouez Nàzgüls, Black Breath (NC : 207) : quand l'adversaire avance, il reçoit une blessure et avec  YBGE,  il ne peut se soigner aux sanctuaires ( et pas du tout si vous avez joué Black Breath). Il est possible de faire jusqu'à huit blessures ainsi, si vous êtes assez chanceux pour avoir UTWE en jeu dès le premier tour. (Personnellement, j'en joue quatre)
<p>
Quelques sites font aussi des blessures.  Si les dégâts intensifs sont votre but, les meilleurs sites sont :<br>
Midgewaters Marshes. (Chaque Hobbit doit s'exerter) NC : 332<br>
Dwarrowdelf Chamber.(Si dans votre zone personne ne joue Gimli) NC : 344. Ou Moria Lake (NC : 346)<br>
Silverlode Banks. (Débarassez-vous des rangers de l'adversaire avant qu'il n'y arrive)  NC : 355.<br>
Anduin Banks (ajoute de nombreux points d'archerie). NC : 356.
<p>
Comme l'adversaire sera sérieusement blessé, jouez quelques minions ayant damage +1, et vous devriez tuer plusieurs compagnions sans trop de difficultés.
<p>
Bien sûr, vous pouvez plutôt choisir d'ajouter des points de corruption au porteur de l'anneau.<br>
Voici les moyens de procéder :<br>
Forcer le porteur de l'anneau à mettre l'anneau et ensuite lui faire des blessures. On peut le forcer à le mettre en lui faisant des dégâts ou en jouant certaines cartes (une seule pour l'instant : Saruman's Reach NC 137).<br>
Jouer une de ces cartes (toutes évidentes d'utilisation ) : <br>
Uruk Spy (NC 155) et Lost To The Goblins (NC 189), Worry (NC 162), Cruel Caradhras (NC 124), Drawn To Its Power (NC 211), In The RingWraith's Wake (NC 214), Goblin Spear (NC 182).<p>
Mais les meilleures cartes pour ajouter des points de corruption sont :<br>
Desperate Defense of The Ring (NC 244), car il suffit de la jouer sur un puissant compagnion de l'adversaire, Aragorn, Gandalf ou Legolas, et si le FPP assigne trop souvent ce compagnion dans les combats, il gagnera un paquet de points de corruption.<br>	
Thin and Stretched (NC 279). Jouez des cartes pour ralentir le FPP, et ne jouez que quelques puissants minions sur chaque site FPP risque d'être très ennuyé.<br>
Blade Tip (NC 209) peut ajouter des points de corruption si jouée sur le porteur de l'anneau, mais il y a peut de chances de réussir.
<p>
Pour conclure, je dirai que les moyens d'infliger des dégâts directs sont plus nombreux pour le Shadow Player que pour le Free People Player, et que donc, il est mieux de les utiliser dans un deck dont le but est de tuer ou corrompre le Frodo adverse que dans un deck qui essaie de faire atteindre à votre Frodo le site 9.
