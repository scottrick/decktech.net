<br />
If you&#8217;re looking for some kind of flashy attack on the world&#8217;s #1 ranked player, you won&#8217;t find that here, so stop reading before you&#8217;re disappointed. In his first article, Brian Hunter did not provide much ammunition for a strong rebuttal, mainly because everything he writes is so factual and contains very little opinion. It&#8217;s difficult to disagree with the truth. In fact, it is interesting that our articles contained largely the same material; obviously, we did not cooperate in writing these articles, which some people have pointed out to be disappointingly similar. The fact that they are should mean something significant. Hopefully in the future we will have some drastically differing points of view, since that would be a more interesting read (and make for a better rebuttal).<br />
<br />
I guess my main point of contention is with Hunter&#8217;s assertion that the dark Hoth deck is impotent when it doesn&#8217;t flip:<br />
<br />
If you cannot get the objective flipped, then life is BAD.  You have no battleground sites on Hoth at all, so Battle Plan is crippling you.  You can&#8217;t deploy to most of your sites, because of the energy shield, but your opponent can.  Your opponent has not needed to lose force for the MPG being destroyed (since it hasn&#8217;t been), and they don&#8217;t need to lose any force to YMSYL.  Your force drain bonuses CAN be cancelled, you can&#8217;t take a card from lost pile into hand when you<br />
deploy an At-At, you don&#8217;t get the battle destiny bonus at the system, and your walkers are not destiny +1.  You are the one in a world of hurt.<br />
<br />
 I disagree with this statement. The objective clearly has flipping in mind; all the bonuses on the front occur only on Hoth (such as the walker deploy bonus) and the cards you pull are largely only good in a walker deck. But a deck could be designed to take advantage of Imperial Decree and the fact that a battleground system starts with the objective. Additionally, with the shields in play at the beginning, you will only ever suffer a drain of 1 at your starting 2/0 the Ice Plains, which isn&#8217;t a bad deal. Compare the deck with Set Your Course for Alderaan, which rarely ever flips. Both objectives start a useless card (Alderaan and the shields). Set Your Course provides 5 force icons, as does this. It is arguable that Imperial Occupation is slightly more balanced by being more immune to WYS&#8217;s force choke text, and the benefits of Decree are innumerable. SYCFA provides a strong battleground site for the purposes of Battle Plan, but you can&#8217;t drain there. Imperial Occupation provides a solid planet to drain at that is also good also for Battle Plan, but you are not (obviously) immune to damage there. So both objectives have pros and cons as a base for a mains platform or other deck.<br />
<br />
I could see something that started both Decree and Prepare for a Surface Attack. Set up using Blizzard 4 (deploys -1) to pull Captain Lennox. This also lets you deploy a Star Destroyer for cheap. If you include Imperial Commands to fetch Piett/Merrejk and set up a free Executor at Bespin, you could conceptually deploy Death Squadron Star Destroyers for virtually nothing at Hoth and spread to other systems to establish a strong space presence. Merrejk would pull all the systems you wanted and something as simple as a Presence of the Force on the Ice Plains would provide the site for Battle Plan and free drains the rest of the game. Most of the negatives from Hunter&#8217;s statement are bonuses you don&#8217;t get from flipping, which is logically flawed. It&#8217;s like saying WYS is no good because you can&#8217;t deploy Ben Kenobi, or Throne Room Mains is a bad deck because you don&#8217;t train Luke to 6. <br />
<br />
There are other minor details that are wrong in Hunter&#8217;s article, such as occupying sites reducing the destiny required for Target The Main Generator, but I believe these are merely slips and do not detract from the points he is making.<br />
<br />
In future installments we will likely look at how cards from this new set impact the meta and what decks to expect/what cards to see in them. I think you can all expect juicier opinions from the both of us from those. <br />
