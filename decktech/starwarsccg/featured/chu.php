Article #2 - The SWCCG Classic Format<br />
<br />
This article will discuss the new Classic tournament format, its pros and cons, and what will be necessary to get it really going (and spark interest). There are no deck ideas below.<br />
<br />
The rules:<br />
<br />
*******<br />
All cards produced by Decipher and the Player&#8217;s Committee are legal except the following:<br />
Any cards with the Episode 1 icon<br />
Watch Your Step/This Place Can Be A Little Rough<br />
All characters with permanent weapons<br />
Brangus Glee/Ketwol<br />
Any cards with a maintenance cost<br />
Any &#8217;insert&#8217; cards<br />
*******<br />
<br />
The main problem with the current Classic format is the same that plagues the game in general: a broad lack of players (compared to the heyday of Wars). Combined with human laziness, there is no incentive right now to take the time to, in many instances, take apart a current deck, figure out the meta, build two new decks that may be totally different than your non-Classic ones, and then go to a tournament where an optimistic estimation for turnout would be in the low single digits. We all have other things to do with our time.<br />
<br />
However, I love the idea of a Classic format &#8211; who wouldn&#8217;t love to see some of their old cards brought out again in ancient decktypes that became eliminated with the advent of WYS and other uber-powerful objectives/cards? <br />
<br />
Before I go into ways to make Classic truly successful, let me touch on the rules laid down by the PC and why I subscribe to most of them, in order of necessity.<br />
<br />
·	Any &#8216;insert&#8217; cards: What nobody wants to see again is Dagobah Cheese. Nobody liked Dagobah Cheese (in case you forgot, it&#8217;s typically: insert Anger Fear Aggression, Visored Vision, go, lose 4, insert AFA, play Uncontrollable Fury, etc). It wasn&#8217;t even fun to play. This effectively destroys that decktype. Visored Vision can be easily grabbed and without the constant 4 force loss due to AFA, coupled with the loss of the Podrace damage as a sure thing, the deck just stalls. Also, I think one of the main points of Classic is to create an environment that brought back good old Mains and Toys without any of the garbage that accompanied them (like Odds). What&#8217;s the best way to do this? Bag the offending cards.<br />
·	All characters with permanent weapons: Yes. Kill EPPs. Some have argued they were a necessity, allowing for more card efficiency in decks. I still think they were a horrible idea, a way for Decipher to unload Unlimited and force tournament players to spend additional money. If you want to make sure Obi-Wan gets his saber, the better way to do this would have been to create virtual Blaster Rack ages ago. This rule will ensure Son of Skywalker sees some daylight. What&#8217;s bad about this rule? Court becomes very difficult to play. But be sure some of the cards the PC will &#8216;review&#8217; in the future are those currently crappy guys from Dagobah.<br />
·	Watch Your Step: this is necessary for the sole reason of slowing the game down. One of the attributes a lot of old decks shared (that helped their spiral into the shoebox) was their speed, or lack thereof, of setup. Watch Your Step completely pummeled Ralltiir Ops, or any mains deck that was the least bit slow. Although weakened by the loss of EPP Han, Enraged Chewie and Lando with gun, WYS would still outmatch all the slow dark decks, and since no other deck (light or dark) is nearly as quick, its removal is necessary.<br />
·	Any cards with a maintenance cost: Eh, as much as I love playing with 5-destiny monster Lando, it&#8217;s a little too big for this environment. It&#8217;s all about balance. These cards are so big they create their own weather. They gotta go.<br />
·	Brangus Glee/Ketwol: I was going to address this as something that I disliked about the rules, but then it occurred to me not only were shields unplayable, A Tragedy Has Occurred (the non-shield) was also. And then it occurred to me also that the big games I lost with Brangus were because of podracing, and that&#8217;s gone too. Brangus was so good&#8230; but I guess this is necessary, mainly because that wasn&#8217;t how he was supposed to function, so the PC has to close this loophole.<br />
<br />
The only rule I disagree somewhat with is the last one: All cards with Episode I icon. The main reason is because I think the Theed Palace objectives which never see the light of day might do well in Classic. There are also a host of cards useful in any deck, and I don&#8217;t mean shields. I mean stuff like Wipe Them Out, All Of Them, or A Tragedy Has Occurred. It would take me pages to list them. To exclude all of them just to eliminate some other strategies like Podracing, or even Senate and Combat, seems wasteful. But I guess I would rather see a broad rule such as this that excludes everything than a custom-tailored list of &#8220;banned&#8221; cards that make the undesirable strategies impossible to play. Those are unwieldy and create confusion, not to mention that people have to carry it around with them to remember exactly which cards can/cannot be used. So at the expense of the Theed Palace objective-loving minority, all the Episode I cards are disallowed.<br />
<br />
<br />
Some of the questions I was asked to discuss concerning Classic were regarding whether the current form works or not. Whether it is successful or not depends on testing it in a large environment, where there will be a big incentive to come up with the absolute best decks possible (including possible broken strategies) &#8211; only then will the rules and format be truly tested. I can&#8217;t say whether or not it will work until then.<br />
<br />
To do this I think the PC or any other large-tournament organizing force must create an incentive to get double-digit players together in a Classic environment. One possibility that has come up is whether or not to make Classic a format for one of the qualifying days at Worlds this year. I think that&#8217;s a great idea. Another I would hope to see sometime would be a big-style tournament, possibly a DPC or Mini-open, that is Classic only. I for one am lazy as hell, and in a tournament where there are two constructed formats, I doubt I will ever want to make four separate decks, two for each format, and I would guess a lot of players are in the same boat. If there were a money tournament or something that sported a large prize guaranteed to attract many players, I wouldn&#8217;t really care what format it was, whether Classic or not. It would be fun to rebuild old decks and play them.<br />
<br />
Now a tangent. As the con season starts up and summer approaches, likely there will be an influx of medium to large regional events. It seems to me that there are actually a decent amount of younger players who have always been playing but have been unable to play competitively due to age, or people who have just gotten into the game (because it&#8217;s so cheap to get all the cards) or are returning for whatever reason. In addition to large prizes, I would like to see prizes for best new player, or best under 17, or even something more rigid like a $100 prize for best player <1600, $100 for between 1601-1800, 1801-2000, etc like there are at chess tournaments. A complaint I&#8217;ve heard is at events like DPCs, there are actually many more people present than the numbers in a tournament indicate because a lot of people aren&#8217;t willing to pay $25 and have very little shot of winning the whole thing. This is a shame. To me, if the top prize of $500 is split up between 5 ratings brackets or age brackets or however a tournament director wants to do it, and this means 20-30 more players play, then everyone wins. I know a lot of tournament directors already do things to encourage younger or new players, such as dividing prize support so younger players get first pick, or by giving more packs to the lower-ranked players; I applaud this. But I have not seen this done very often at all at bigger tournaments, and there is no reason not to. It may just be enough to attract those competitive lower-ranked players into making a trip to your tournament to have a shot at a decent prize.<br />
<br />
Back to Classic. Another critically important factor to making sure Classic works is for the tournament director running a Classic event to really put into the hands of the players the rules and guidelines. Post them in the store, or email them to a listserv. Make sure, because people are stupid or forgetful, and the last thing you want is for someone to show up with decks and not be able to play. I was at Vermont regionals two years ago and some kid showed up with a BHBM deck packing 10 Luke Seekers. Needless to say, he got pissed and left. I guess it&#8217;s his own fault for not knowing those cards were errata&#8217;d, but it&#8217;s as much the company&#8217;s problem for having to issue the errata. The tournament director should make sure the rules are widespread, although it is not his responsibility to do so. <br />
<br />
Lastly, for this to work: GET INVOLVED! Get excited. Bespin and Beyond was awesome. In Albany we did a Bespin and Before event and that kicked ass too. Remember those Bespin scenario tournaments with the point structures, and you got a full win only if you did certain things to score points? If you never played in one of those, run one. They are some of the best memories from all my SW:CCG experiences. If you stand by, then these events won&#8217;t be successful. Get off your ass, make some decks, and play!<br />
