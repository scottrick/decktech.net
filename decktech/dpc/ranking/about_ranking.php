                  <p><font face="Arial, Helvetica, sans-serif" size="2"><b>The 
                    DPC Rating System: </b><br>
                    The DPC ranking system will use the ELO system. The ELO system 
                    is used by several professional sports organizations to rank 
                    players. The system is designed to give accurate rankings 
                    for a large group of contestants for continuous play. New 
                    players can join at any time, and as they play games, they 
                    will float to their proper place in the seeding. <br>
                    <br>
                    However, unlike many other CCG rating systems the DPC ranking 
                    system is designed to reflect the player’s current proficiency 
                    by keeping track of how long it has been since they played 
                    in a major event. Under the DPC ranking system if a player 
                    stops competing for a period of time their rating will slowly 
                    decrease as to allow current players to pass them. <br>
                    <br>
                    Here is how it works. When a player competes in his or her 
                    first DPC event they will start with a Lifetime Proficiency 
                    (LP) of 1500 and a Current Proficiency (CP) of 1500. Thus, 
                    every new competitor starts with a Combined Rating (CR) of 
                    3000. <br>
                    <br>
                    Your LP will be calculated every tournament using the ELO 
                    system. This system will determine the amount your LP will 
                    increase or decrease. Your CP will also increase or decrease 
                    by an identical amount. At the end of every month that a DPC 
                    event took place the DPC ranking system will subtract 30 points 
                    from your CP if you did not participate in a sanctioned DPC 
                    event that month. <br>
                    <br>
                    For example you win 6 games at the first tournament your LP 
                    rating becomes 1564 and your CP rating becomes 1564. Your 
                    combined rating is now 3,128. The next month, if there is 
                    a DPC event scheduled and you don’t play in it your CP is 
                    reduced by 30 to 1534 to represent the period of time you 
                    were absent from competition. Your CR is now 3098. <br>
                    <br>
                    All tournaments at DPC events will count toward your rating. 
                    Separate ratings will be kept for sealed deck and constructed 
                    formats. Your Overall Rating is the sum of all of your Combined 
                    Ratings for each format divided by the number of formats. 
                    If you choose not to participate in a particular format you 
                    will remain at the entry point for that format (1500). <br>
                    <br>
                    It should be noted though that the ratings themselves do not 
                    qualify you for any additional events.<br>
