<h1><span style='font-size:12.0pt'>Getting Started<o:p></o:p></span></h1>

<p class=MsoBlockText style='margin-left:0in'><b>Step 1: Personalize your
homepage<o:p></o:p></b></p>

<p class=MsoBlockText style='margin-left:.5in'>The first step to getting your
group started is laying out your homepage.<span style="mso-spacerun: yes"> 
</span>When you signed up for your new group you were given the default
homepage configuration. The welcome message was on the top and nothing beneath
it. </p>

<p class=MsoBlockText style='margin-left:.5in'>Go to the bottom of your
homepage and find the Center Column layout manager. Use the top 3 select boxes
and choose News, Message Board, and Our Games, and click the update button. The
default message will be replaced with two empty components and information
about the games you play.</p>

<p class=MsoBlockText style='margin-left:.5in'>Take a few minutes to add a new
story to the News by clicking on “[add]”. Write something simple like the
reason your starting this group and who should join. Click “Submit”.
Congratulations you have added your first news story!</p>

<p class=MsoBlockText style='margin-left:.5in'>Don’t worry if it’s perfect you
can always go back and edit it later. Next add a new topic on the message board
asking people to let you know when they joined by posting on the board. </p>

<p class=MsoBlockText style='margin-left:.5in'>The last thing to do is pick
your site theme. Go to the bottom left hand column of the page and select a new
theme from the last select box in that column. Click “Update”, to see the new
theme. While your there you should probably put your Join box up if your
planning on allowing members to request to join your group without you having
to send them an invitation. </p>

<p class=MsoBlockText style='margin-left:.5in'>The join box can be found in the
top select box of the left hand column only. Select it and click “Update”. </p>

<p class=MsoBlockText style='margin-left:.5in'>Now your ready to start inviting
members!</p>

<p class=MsoBlockText style='margin-left:0in'><b>Step 2: Inviting Members<o:p></o:p></b></p>

<p class=MsoBlockText style='margin-left:.25in'>Inviting members is easy but
you must follow a few simple rules. </p>

<p class=MsoBlockText style='margin-left:.75in;text-indent:-.25in;mso-list:
l1 level1 lfo3;tab-stops:list .75in'><![if !supportLists]>1.<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span><![endif]>You
can only invite DeckTech members. The only way to enable you to have a site for
your members only is to have them “log in” in such a way that differentiates
them from all the other passersby. In order to log in they must have a DeckTech
account. Signing up is free and only takes a minute so get your group members
to sign up, so you can invite them.</p>

<p class=MsoBlockText style='margin-left:.75in;text-indent:-.25in;mso-list:
l1 level1 lfo3;tab-stops:list .75in'><![if !supportLists]>2.<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span><![endif]>You
can only invite a member once. No spamming allowed!</p>

<p class=MsoBlockText style='margin-left:.75in;text-indent:-.25in;mso-list:
l1 level1 lfo3;tab-stops:list .75in'><![if !supportLists]>3.<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span><![endif]>Invitations
are emailed to the email address the member registered with when they got their
account. If they have changed their email address since then they will need to
update their information before your invitations can get to them. They can
click “Update My Info” in the member center on the homepage to update their
email address.</p>

<p class=MsoBlockText style='margin-left:.25in'>Lets begin! First ask yourself
if your group is geographically based (city, state etc)? </p>

<p class=MsoBlockText style='margin-left:.25in'>If so begin by mapping a zip
code to your group by clicking on “Map Zip Codes” in the Admin box. Add a zip
code near your group by putting it in the box and clicking “Add”.
Congratulation you just registered your group with that zip code. Now each time
a new person joins DeckTech with that zip code you will be notified that
someone new is in your area!</p>

<p class=MsoBlockText style='margin-left:.25in'>After your done adding a few
zip codes click on “Invite Members”.</p>

<p class=MsoBlockText style='margin-left:.25in'>Now go down to the “Select
DeckTech Members By” box and select “In My Zip Codes”. The page will reload
with all the DeckTech members that live in your zip codes displayed in the
larger select box. Select all the members you want to invite. </p>

<p class=MsoBlockText style='margin-left:.25in;tab-stops:.25in'>Click “Invite”
to invite all selected members. You will get a message letting you know that
your invitations went out. Now click on the “Manage Members” link in the Admin
Box. You should be able to see a list of all invited members displayed on that
page. Once you have invited all the members you wish, just sit back and wait
for them to join.</p>

<p class=MsoBlockText style='margin-left:0in'><b>Step 3: Managing Members</b></p>

<p class=MsoBlockText style='margin-left:.5in'>After a few people have
responded to your invitations you will notice that the system moves them from
the invited members location to the Current Members location on the “Manage
Members” page. To help maintain the site pick a few people you trust and make
them either Admin or Reporters. </p>

<p class=MsoBlockText style='margin-left:.5in'>After you have a few people
helping you with the site your well on your way to making your site a success!
To get involved in current groups development and to ask questions or make
suggestions join the <a href="http://www.decktech.net/groups/h.php?g=41"><span
style='color:windowtext'>Group for Group Leaders</span></a>. </p>

