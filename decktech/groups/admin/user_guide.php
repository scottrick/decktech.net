sections can be found in the left hand column of the page. To find it look for
the word “Admin” and a link named “[hide]” in this box’s headline. <br>
<br>
This box contains links to all the admin specific sections of your group site
and is only visible by site the admin. To see the site as your regular group
members see it you can click the link “[hide]” and the admin box and all admin
specific components will be removed from the page. To view the page in normal
admin mode again simply go back in your browser, or leave the page and return,
all admin functions will be restored as before.<o:p></o:p></span></p>

