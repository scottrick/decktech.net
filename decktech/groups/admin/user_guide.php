The ability to update, administer and run your group site is provided by simple
but robust administration tools located in a few easy to find locations. You
can find these tools located in the admin navigation box, in the layout manager
at the bottom of each page, and contextually in each group component that
allows administration.<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>

<p class=MsoNormal><span style='font-size:9.0pt;mso-bidi-font-size:12.0pt;
